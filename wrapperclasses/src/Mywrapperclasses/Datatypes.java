package Mywrapperclasses;

public class Datatypes {

	public static void main(String[] args) {
		//Program about all datatypes usage
int a = 10;
float b = 1.1f;
double c = 12.12d;
char d = 'N';
short e = 5;
long f = 1234567890;
byte g = 4;
boolean h = true;
Integer i = new Integer(a);
Float j = new Float(b);
Double k = new Double(c);
Character q = new Character(d);
Short u = new Short(e);
Long v = new  Long(f);
Byte y = new Byte(g);
Boolean z = new Boolean(h);
System.out.println("Integer is "+ i);
System.out.println("Float is "+j);
System.out.println("Double is "+k);
System.out.println("Character is "+q);
System.out.println("Short is "+u);
System.out.println("Long is "+v);
System.out.println("Byte is "+y);
System.out.println("Boolean is "+z);
	}
 
}
